function tableToString(tbl, indent)
	indent = indent or 0
	local result = "{\n"
	local spacing = string.rep("    ", indent + 1)

	for k, v in pairs(tbl) do
		local keyStr
		if type(k) == "string" then
			keyStr = string.format("[%q]", k)
		else
			keyStr = string.format("[%s]", tostring(k))
		end

		local valueStr
		if type(v) == "table" then
			valueStr = tableToString(v, indent + 1)
		elseif type(v) == "string" then
			valueStr = string.format("%q", v)
		else
			valueStr = tostring(v)
		end

		result = result .. spacing .. keyStr .. " = " .. valueStr .. ",\n"
	end

	result = result .. string.rep("    ", indent) .. "}"
	return result
end
